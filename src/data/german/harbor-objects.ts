export function polygonToSvgPoints(polygon: Polygon): string {
  const points: string[] = [];
  for (let i = 0; i < polygon.length; i += 2) {
    points.push(`${polygon[i]},${polygon[i + 1]}`);
  }
  return points.join(' ');
}